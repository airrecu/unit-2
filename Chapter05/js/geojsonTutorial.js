// creates a variable named map, 
// map() associates it to the "map" div
// setView() assigns to it an object wtih properites for center and zoom  

var map = L.map('map').setView([39.75621, -104.99404], 4);



// tilelayer() function loads and displays a tilelayer with options set for zoom and other features
// addTo()  displays the tilelayer in the map div
L.tileLayer('https://stamen-tiles-{s}.a.ssl.fastly.net/watercolor/{z}/{x}/{y}.{ext}', {
	attribution: 'Map tiles by <a href="http://stamen.com">Stamen Design</a>, <a href="http://creativecommons.org/licenses/by/3.0">CC BY 3.0</a> &mdash; Map data &copy; <a href="https://www.openstreetmap.org/copyright">OpenStreetMap</a> contributors',
	subdomains: 'abcd',
	minZoom: 1,
	maxZoom: 16,
	ext: 'jpg'
}).addTo(map);


// onEachFeature() gives popup and events to already created featrues
// bindPopup() gives the layer a popup message given as the parameter
function onEachFeature(feature, layer) {
    // does this feature have a property named popupContent?
    if (feature.properties && feature.properties.popupContent) {
        layer.bindPopup(feature.properties.popupContent);
    }
}

var geojsonFeature = {
    "type": "Feature",
    "properties": {
        "name": "Coors Field",
        "amenity": "Baseball Stadium",
        "popupContent": "This is where the Rockies play!"
    },
    "geometry": {
        "type": "Point",
        "coordinates": [-104.99404, 39.75621]
    }
};


// geoJSON() This function creates a GeoJSON layer by passing an object in GeoJSON format
L.geoJSON(geojsonFeature, {
    onEachFeature: onEachFeature
}).addTo(map);


